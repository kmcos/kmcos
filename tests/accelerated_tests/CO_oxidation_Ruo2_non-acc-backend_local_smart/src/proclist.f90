subroutine put_CO_ruo2_bridge(site)

    integer(kind=iint), dimension(4), intent(in) :: site

    ! update lattice
    call replace_species(site, empty, CO)

    ! disable affected processes
    if(avail_sites(Ads_bridge_down, lattice2nr(site(1) + (0), site(2) + (-1), site(3) + (0), site(4) + (0)), 2).ne.0)then
        call del_proc(Ads_bridge_down, site + (/0, -1, 0, 0/))
    endif

    if(avail_sites(Ads_bridge_down, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(Ads_bridge_down, site)
    endif

    if(avail_sites(Ads_bridge_left, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(Ads_bridge_left, site)
    endif

    if(avail_sites(Ads_bridge_right, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(Ads_bridge_right, site)
    endif

    if(avail_sites(Ads_bridge_up, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(Ads_bridge_up, site)
    endif

    if(avail_sites(Ads_bridge_up, lattice2nr(site(1) + (0), site(2) + (-1), site(3) + (0), site(4) + (0)), 2).ne.0)then
        call del_proc(Ads_bridge_up, site + (/0, -1, 0, 0/))
    endif

    if(avail_sites(Ads_cus_left, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(Ads_cus_left, site)
    endif

    if(avail_sites(Ads_cus_right, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(Ads_cus_right, site)
    endif

    if(avail_sites(CO_adsorption_bridge, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(CO_adsorption_bridge, site)
    endif

    if(avail_sites(COdiff_bridge_down, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(COdiff_bridge_down, site)
    endif

    if(avail_sites(COdiff_bridge_left, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(COdiff_bridge_left, site)
    endif

    if(avail_sites(COdiff_bridge_up, lattice2nr(site(1) + (0), site(2) + (-1), site(3) + (0), site(4) + (0)), 2).ne.0)then
        call del_proc(COdiff_bridge_up, site + (/0, -1, 0, 0/))
    endif

    if(avail_sites(COdiff_cus_right, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(COdiff_cus_right, site)
    endif

    if(avail_sites(O2_adsorption_bridge_right, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(O2_adsorption_bridge_right, site)
    endif

    if(avail_sites(O2_adsorption_bridge_up, lattice2nr(site(1), site(2), site(3), site(4)), 2).ne.0)then
        call del_proc(O2_adsorption_bridge_up, site)
    endif

